# Se define cada una de las variables a emplear
num = 40

# Se emplea el uso de operadores de asignacion para resolver operaciones especificas en una cantidad especifica
print("numero original ", num )
num+=2
print("numero + 2 ", num )
num-=5
print("numero - 5 ", num )
num/=4
print("numero / 4 ", num )
num**=2
print("numero ** 2 ", num )
num//=10
print("numero // 10 ", num )
num%=2
print("numero % 2 ", num )